"""
prediction.py

Prediction database model.

Stores crop recommendation history.
"""

from datetime import datetime
from app.extensions import db


class Prediction(db.Model):
    """
    Prediction Model.

    Stores every crop recommendation
    generated by the ML model.
    """

    # --------------------------------
    # Database Table Name
    # --------------------------------

    __tablename__ = "predictions"

    # --------------------------------
    # Primary Key
    # --------------------------------

    id = db.Column(
        db.Integer,
        primary_key=True
    )

    # --------------------------------
    # User Relationship
    # --------------------------------

    # Foreign key linking prediction
    # to the user who created it.

    user_id = db.Column(
        db.Integer,
        db.ForeignKey("users.id"),
        nullable=False
    )

    # --------------------------------
    # Prediction Result
    # --------------------------------

    crop = db.Column(
        db.String(100),
        nullable=False
    )

    confidence = db.Column(
        db.Float
    )

    # --------------------------------
    # Soil Data
    # --------------------------------

    N = db.Column(
        db.Float,
        nullable=False
    )

    P = db.Column(
        db.Float,
        nullable=False
    )

    K = db.Column(
        db.Float,
        nullable=False
    )

    ph = db.Column(
        db.Float,
        nullable=False
    )

    # --------------------------------
    # Climate Data
    # --------------------------------

    temperature = db.Column(
        db.Float,
        nullable=False
    )

    humidity = db.Column(
        db.Float,
        nullable=False
    )

    rainfall = db.Column(
        db.Float,
        nullable=False
    )

    # --------------------------------
    # Timestamp
    # --------------------------------

    created_at = db.Column(
        db.DateTime,
        default=datetime.utcnow,
        nullable=False
    )

    # --------------------------------
    # Object Representation
    # --------------------------------

    def __repr__(self):
        return (
            f"<Prediction {self.crop}>"
        )